package CursoLogicaCapitulo03;

public class TiposNumericos {

	public static void main(String[] args) {
		// Inteiros 
		Byte numeroByte = 1; // Vai de -128 até 127

		Short numeroShort = 1; // Vai de -32768 até 32767

		Integer numeroInteger = 2147483647; // Vai de -2147483648 até 2147483647

		Long numeroLong = 1L;

		// Decimais
		
		Float numeroFloat = 1.0f;
		
		Double numeroDouble = 1.0;
	}

}
